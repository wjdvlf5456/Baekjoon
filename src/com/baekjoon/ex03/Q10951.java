package com.baekjoon.ex03;

import java.util.Scanner;

public class Q10951 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		while (sc.hasNext()) {
			int numA = sc.nextInt();
			int numB = sc.nextInt();

			System.out.println(numA + numB);
		}

		sc.close();

	}

}
